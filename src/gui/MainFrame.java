    private void priBrowseButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_priBrowseButtonActionPerformed
        JFileChooser jfc = new JFileChooser();
        FileNameExtensionFilter filter = new FileNameExtensionFilter("Private Key Files", "pri");
        jfc.setFileFilter(filter);
        int res = jfc.showOpenDialog(this);
        if (res == JFileChooser.APPROVE_OPTION) {
            String path = jfc.getSelectedFile().getPath();
            try {
                IO.getInputString(path, "pri");
                Path pat = Paths.get(path);
                String filename = pat.getFileName().toString();
                privatePathLabel.setText(filename);
            } catch (FileNotFoundException ex) {
                Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_priBrowseButtonActionPerformed

    private void generateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_generateButtonActionPerformed
        
        BigInteger a = new BigInteger(aTextField.getText());
        BigInteger b = new BigInteger(bTextField.getText());
        BigInteger p = new BigInteger(pTextField.getText());
        ec = new EllipticCurve(a, b, p);
        Point base = ec.solveForY(new BigInteger("11"), new BigInteger("20"));
        ec.setBase(base);
        
        ecceg = new ECCEG(ec);
        
        JFileChooser jfc = new JFileChooser();
        int res = jfc.showSaveDialog(this);
        if (res == JFileChooser.APPROVE_OPTION) {
            try {
                FileWriter fw = new FileWriter(jfc.getSelectedFile() + ".pri");
                System.out.println("pri key: " + ecceg.getPrivateKey());
                fw.write(ecceg.getPrivateKey().toString());
                fw.close();
                
                fw = new FileWriter(jfc.getSelectedFile() + ".pub");
                fw.write(ecceg.getPublicKey().getX() + "\n" + ecceg.getPublicKey().getY());
                fw.close();
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_generateButtonActionPerformed

