    private void encryptButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_encryptButtonActionPerformed
        
        // Init Elliptic Curve Equation
        BigInteger a = new BigInteger(aTextField.getText());
        BigInteger b = new BigInteger(bTextField.getText());
        BigInteger p = new BigInteger(pTextField.getText());
        ec = new EllipticCurve(a, b, p);
        Point base = ec.solveForY(new BigInteger("11"), new BigInteger("20"));
        ec.setBase(base);
        
        ecceg = new ECCEG(ec);
        
        Point[] encoded;
        Point[][] encrypted;
        
        // Encode plain text to points
        encoded = Koblitz.encode(ECCEG.plain, ec);
        ECCEG.encoded = encoded;
        
        // Encrypt points
        encrypted = new Point[encoded.length][2];
        BigInteger K = Gen.generateK(ec.getP());
        for (int x=0; x<encoded.length; x++) {
            encrypted[x] = ecceg.encrypt(encoded[x], K);
        }
        
        ECCEG.encrypted = encrypted;
        
        String enc = ecceg.getEncryptedString();
        cipherTextArea.setText(enc);
    }//GEN-LAST:event_encryptButtonActionPerformed

